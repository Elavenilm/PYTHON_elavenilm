# Bitwise Operator:

A = 10
B = 20

# bitwise AND operator:                         #Output: A & B = 0
print("A & B = ",A&B)     

# bitwise OR operator:                          #Output: A | B = 30
print("A | B = ",A|B)

# bitwise XOR operator:                         #Output: A ^ B = 30
print("A ^ B = ",A^B)

# bitwise NOT operator:                         #Output: ~A = -11
print("~A = ",~A)

# bitwise shift:
A = 10
B = -10
# bitwise right shift:
print("A >> 1 = ",A>>1)                         #Output: A >> 1 =  5
print("B >> 1 = ",B>>1)                         #Output: B >> 1 =  -5

#bitwise left shift:
print("A << 1 = ",A<<1)                         #Output: A << 1 =  20
print("B << 1 = ",B<<1)                         #Output: B << 1 =  -20