# Assignment Operators:

A = 20
B = 10

# addition assignment operator:                     #Output: 30
C= A + B
print(C)
A += B       #A=A+B
print(A)

# subtraction assignment operator:                  #Output: 10
C= A - B
print(C)
A -= B       #A=A-B
print(A)

# multiplication assignment operator:               #Output: 200
C= A * B 
print(C)
A *= B       #A=A*B
print(A)

# division assignment operator:                     #Output: 2.0
C= A / B 
print(C)
A /= B       #A=A/B
print(A)

# modulus assignment operator:                      #Output: 0
C= A % B 
print(C)
A %= B       #A=A%B
print(A)

# floor division assignment operator:               #Output: 2
C= A // B 
print(C)
A //= B       #A=A//B
print(A)

# exponentiation assignment operator:               #Output: 10240000000000
C= A ** B 
print(C)
A **= B       #A=A**B
print(A)

# bitwise AND assignment operator:                  #Output: 0
C= A & B 
print(C)
A &= B       #A=A&B
print(A)

# bitwise OR assignment operator:                   #Output: 30
C= A | B 
print(C)
A |= B       #A=A|B
print(A)

# bitwise XOR assignment operator:                  #Output: 30
C= A ^ B 
print(C)
A ^= B       #A=A^B
print(A)

# bitwise right shift assignment operator:          #Output: 0
C= A >> B 
print(C)
A >>= B       #A=A>>B
print(A)

# bitwise left shift assignment operator:          #Output: 20480
C= A << B 
print(C)
A <<= B       #A=A<<B
print(A)