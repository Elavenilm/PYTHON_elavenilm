# Comparison Operators:

A = 20
B = 10

# Greater than:                                              #Output: Greater than A > B: True
print("Greater than A > B:", A > B)

# Less than:                                                 #Output: Less than A < B: False
print("Less than A < B:", A < B)

# Greater than or equal to:                                  #Output: Greater than or equal to A >= B: True
print("Greater than or equal to A >= B:", A >= B)

# Less than or equal to:                                     #Output: Less than or equal to A <= B: False
print("Less than or equal to A <= B:", A <= B)

# Equal to:                                                  #Output: Equal to A == B: False
print("Equal to A == B:", A == B)

# Not equal to:                                              #Output: Not equal to A != B: True
print("Not equal to A != B:", A != B)