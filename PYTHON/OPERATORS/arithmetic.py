# Arithmetic Operators:

A = 20
B = 10

# addition operator:                                       #Output: addition: 30
result= A + B
print("addition:",result)

# subtraction operator:                                    #Output: subtraction: 10
result= A - B
print("subtraction:",result)

# multiplication operator:                                 #Output: multiplication: 200
result= A * B
print("multiplication:",result)

# division operator:                                       #Output: division: 2.0
result= A / B
print("division:",result)

# floor division operator:                                 #Output: floor division: 2
result= A // B
print("floor division:",result)

# modulus operator:                                        #Output: modulus: 0
result= A % B
print("modulus:",result)

# exponentiation operator:                                 #Output: exponentiation: 10240000000000
result= A ** B
print("exponentiation:",result)