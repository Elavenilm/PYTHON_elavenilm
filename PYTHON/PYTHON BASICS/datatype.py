# GETTING THE DATA TYPE BY USING THE type() FUNCTION

#example:

# 1.int:
x=5
print(type(x))

# 2.string:
x="hello world"
print(type(x))

# 3.float:
x=20.5
print(type(x))

# 4.complex:
x=1j
print(type(x))

# 5.list:
x=["apple","banana","kiwi"]
print(type(x))

# 6.tuple:
x=("apple","banana","kiwi")
print(type(x))

# 7.range:
x=range(6)
print(type(x))

# 8.set:
x={"name:hari","age:25"}
print(type(x))

# 9.frozenset:
x=frozenset({"appple","banana","orange"})
print(type(x))

# 10.bool:
x=True
print(type(x))

# 11.bytes:
x=b"Hello"
print(type(x))

# 12.bytearray:
x=bytearray(5)
print(type(x))

# 13.memoryview:
x=memoryview(bytes(5))
print(type(x))

# 14.nonetype:
x=none
print(type(x))